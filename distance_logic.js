/**
 * haversine 함수
 * 위도 경도 값을 토대로 거리를 계산하는 데 쓰임
 * 출력값은 km 단위 (ex. 500m => 0.5)
 * 파라미터 (위도1, 경도1, 위도2, 경도2)
 */
function haversine(lat1, lon1, lat2, lon2) {
  if (lat1 == lat2 && lon1 == lon2) return 0;

  var radLat1 = (Math.PI * lat1) / 180;
  var radLat2 = (Math.PI * lat2) / 180;
  var theta = lon1 - lon2;
  var radTheta = (Math.PI * theta) / 180;
  var dist =
    Math.sin(radLat1) * Math.sin(radLat2) +
    Math.cos(radLat1) * Math.cos(radLat2) * Math.cos(radTheta);
  if (dist > 1) dist = 1;

  dist = Math.acos(dist);
  dist = (dist * 180) / Math.PI;
  dist = dist * 60 * 1.1515 * 1.609344 * 1000;
  if (dist < 100) dist = Math.round(dist / 10) * 10;
  else dist = Math.round(dist / 100) * 100;

  return dist / 1000;
}

/**
 * findCandidates
 * 탑승자 유저가 지정한 출발지&목적지와 1.5km 이내의 유사성을 가지는 운전자 탐색
 * 해당 운전자의 index값, [출발지( [위도,경도] ), 도착지( [위도,경도] )] 를 출력함
 */
function findCandidates(psgLocation, drvLocations) {
  const similar = [];
  const finalCandidates = [];

  for (let i = 0; i < drvLocations.length; i++) {
    const drvLocation = drvLocations[i];
    const psgDistance = haversine(
      psgLocation[0][0],
      psgLocation[0][1],
      drvLocation[0][0],
      drvLocation[0][1]
    );
    if (psgDistance <= 1.5) {
      similar.push(i);
    }
  }

  for (let i = 0; i < similar.length; i++) {
    const index = similar[i];
    const drvDistance = haversine(
      psgLocation[1][0],
      psgLocation[1][1],
      drvLocations[index][1][0],
      drvLocations[index][1][1]
    );
    if (drvDistance <= 1.5) {
      console.log(index);
      console.log(drvLocations[index]);
      finalCandidates.push(index);
    }
  }

  return finalCandidates;
}

